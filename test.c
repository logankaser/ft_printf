#include "ft_printf.h"
#define PUT(a) write(1, a "\n", sizeof a)

int	main(void)
{
	PUT("\"%c, %c, %c, %c\" | 'a', 'b', 'c', 'd'");
	ft_printf("%c, %c, %c, %c\n", 'a', 'b', 'c', 'd');
	PUT("\"%d, %d, %d, %d\" | 123, -1000, 0, 1");
	ft_printf("%d, %d, %d, %d\n", 123, -1000, 1, 0);
	PUT("\"%x, %X, %o, %O\" | 123, -100, 10, 12");
	ft_printf("%x, %X, %o, %O\n", 123, -100, 10, 12);
	PUT("\"%p\" | 124123432");
	ft_printf("%p\n", 140721966929372);
	PUT("\"%lu\" | 18446744073709551491");
	ft_printf("%lu\n", 18446744073709551491U);
	PUT("\"%C\" | L'€'");
	ft_printf("%C\n", L'€');
	PUT("\"%S\" | L'€'");
	ft_printf("%S\n", L"€€€");
	return (0);
}
